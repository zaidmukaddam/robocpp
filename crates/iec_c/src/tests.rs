#[test]
fn c_emitter_returns_failing_writer_errors() {
    struct FailingWriter {
        remaining: usize,
    }

    impl std::fmt::Write for FailingWriter {
        fn write_str(&mut self, text: &str) -> std::fmt::Result {
            if text.len() > self.remaining {
                self.remaining = 0;
                Err(std::fmt::Error)
            } else {
                self.remaining -= text.len();
                Ok(())
            }
        }
    }

    let mut writer = FailingWriter { remaining: 8 };
    let mut emitter = CEmitter::new(&mut writer);
    emit_communication_abi(&mut emitter);
    assert!(matches!(emitter.check(), Err(CEmitError::Format(_))));
}

fn compile_and_run_c(test_name: &str, source: &str) -> String {
    let dir = env::temp_dir().join(format!("rbcpp_{test_name}_{}", std::process::id()));
    fs::create_dir_all(&dir).expect("temp dir should be created");
    let source_path = dir.join(format!("{test_name}.c"));
    let binary_path = dir.join(test_name);
    fs::write(&source_path, source).expect("C source should be written");

    let compile = Command::new("cc")
        .arg("-std=c11")
        .arg("-Wall")
        .arg("-Wextra")
        .arg(&source_path)
        .arg("-lm")
        .arg("-o")
        .arg(&binary_path)
        .output()
        .expect("cc should be available for backend parity tests");
    assert!(
        compile.status.success(),
        "C compile failed:\n{}",
        String::from_utf8_lossy(&compile.stderr)
    );

    let run = Command::new(&binary_path)
        .output()
        .expect("compiled C parity binary should run");
    assert!(
        run.status.success(),
        "C run failed:\n{}",
        String::from_utf8_lossy(&run.stderr)
    );
    String::from_utf8(run.stdout).expect("C output should be UTF-8")
}

