fn trace_int(variables: &[(String, Value)], name: &str) -> i64 {
    variables
        .iter()
        .find(|(variable, _)| variable == name)
        .and_then(|(_, value)| value.as_i64())
        .unwrap_or_else(|| panic!("missing integer trace variable {name}"))
}

fn trace_bool(variables: &[(String, Value)], name: &str) -> bool {
    variables
        .iter()
        .find(|(variable, _)| variable == name)
        .and_then(|(_, value)| value.as_bool())
        .unwrap_or_else(|| panic!("missing bool trace variable {name}"))
}

fn trace_real(variables: &[(String, Value)], name: &str) -> f64 {
    variables
        .iter()
        .find(|(variable, _)| variable == name)
        .and_then(|(_, value)| value.as_f64())
        .unwrap_or_else(|| panic!("missing REAL trace variable {name}"))
}

fn trace_string(variables: &[(String, Value)], name: &str) -> String {
    variables
        .iter()
        .find(|(variable, _)| variable == name)
        .and_then(|(_, value)| match value {
            Value::String(value) => Some(value.clone()),
            Value::WString(value) => Some(value.clone()),
            _ => None,
        })
        .unwrap_or_else(|| panic!("missing string trace variable {name}"))
}

fn trace_time_ms(variables: &[(String, Value)], name: &str) -> i128 {
    variables
        .iter()
        .find(|(variable, _)| variable == name)
        .and_then(|(_, value)| match value {
            Value::TimeMs(value) => Some(*value),
            _ => value.as_i64().map(i128::from),
        })
        .unwrap_or_else(|| panic!("missing TIME trace variable {name}"))
}

fn trace_struct_int(variables: &[(String, Value)], name: &str, field: &str) -> i64 {
    variables
        .iter()
        .find(|(variable, _)| variable == name)
        .and_then(|(_, value)| match value {
            Value::Struct(fields) => fields.get(field).and_then(Value::as_i64),
            _ => None,
        })
        .unwrap_or_else(|| panic!("missing integer trace field {name}.{field}"))
}

fn trace_struct_bool(variables: &[(String, Value)], name: &str, field: &str) -> bool {
    variables
        .iter()
        .find(|(variable, _)| variable == name)
        .and_then(|(_, value)| match value {
            Value::Struct(fields) => fields.get(field).and_then(Value::as_bool),
            _ => None,
        })
        .unwrap_or_else(|| panic!("missing bool trace field {name}.{field}"))
}

fn trace_array_int(variables: &[(String, Value)], name: &str, index: usize) -> i64 {
    variables
        .iter()
        .find(|(variable, _)| variable == name)
        .and_then(|(_, value)| match value {
            Value::Array(values) => values.get(index).and_then(Value::as_i64),
            _ => None,
        })
        .unwrap_or_else(|| panic!("missing integer trace element {name}[{index}]"))
}

fn bool_digit(value: bool) -> i32 {
    if value {
        1
    } else {
        0
    }
}

