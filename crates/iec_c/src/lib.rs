fn literal_to_c(literal: &Literal) -> String {
    match literal {
        Literal::Int(value) => value.to_string(),
        Literal::Real(value) => value.to_string(),
        Literal::Bool(value) => {
            if *value {
                "true".to_string()
            } else {
                "false".to_string()
            }
        }
        Literal::String(value) => format!("\"{}\"", value.replace('"', "\\\"")),
        Literal::DurationMs(value) => value.to_string(),
        Literal::Date(value) => parse_date_days(value)
            .map(|value| value.to_string())
            .unwrap_or_else(|| "0".to_string()),
        Literal::TimeOfDay(value) => parse_time_of_day_ms(value)
            .map(|value| value.to_string())
            .unwrap_or_else(|| "0".to_string()),
        Literal::DateAndTime(value) => parse_date_time_ms(value)
            .map(|value| value.to_string())
            .unwrap_or_else(|| "0".to_string()),
        Literal::Typed { value, .. } => value.clone(),
    }
}

fn parse_date_time_ms(input: &str) -> Option<i128> {
    if input.len() < 11 {
        return None;
    }
    let date = parse_date_days(input.get(..10)?)? as i128;
    let time = input.get(10..)?.trim_start_matches(['-', 'T', 't']).trim();
    Some(date * 86_400_000 + parse_time_of_day_ms(time)?)
}

fn parse_date_days(input: &str) -> Option<i64> {
    let mut parts = input.split('-');
    let year = parts.next()?.parse::<i64>().ok()?;
    let month = parts.next()?.parse::<i64>().ok()?;
    let day = parts.next()?.parse::<i64>().ok()?;
    if parts.next().is_some() || !(1..=12).contains(&month) || !(1..=31).contains(&day) {
        return None;
    }
    Some(days_from_civil(year, month, day))
}

fn parse_time_of_day_ms(input: &str) -> Option<i128> {
    let mut parts = input.split(':');
    let hour = parts.next()?.parse::<i128>().ok()?;
    let minute = parts.next()?.parse::<i128>().ok()?;
    let second_part = parts.next()?;
    if parts.next().is_some() || !(0..=23).contains(&hour) || !(0..=59).contains(&minute) {
        return None;
    }
    let (second, millis) = if let Some((seconds, fraction)) = second_part.split_once('.') {
        let mut millis_text = fraction.chars().take(3).collect::<String>();
        while millis_text.len() < 3 {
            millis_text.push('0');
        }
        (
            seconds.parse::<i128>().ok()?,
            millis_text.parse::<i128>().ok()?,
        )
    } else {
        (second_part.parse::<i128>().ok()?, 0)
    };
    if !(0..=59).contains(&second) {
        return None;
    }
    Some(((hour * 60 + minute) * 60 + second) * 1000 + millis)
}

fn days_from_civil(year: i64, month: i64, day: i64) -> i64 {
    let adjusted_year = year - if month <= 2 { 1 } else { 0 };
    let era = if adjusted_year >= 0 {
        adjusted_year
    } else {
        adjusted_year - 399
    } / 400;
    let year_of_era = adjusted_year - era * 400;
    let month_prime = month + if month > 2 { -3 } else { 9 };
    let day_of_year = (153 * month_prime + 2) / 5 + day - 1;
    let day_of_era = year_of_era * 365 + year_of_era / 4 - year_of_era / 100 + day_of_year;
    era * 146_097 + day_of_era - 719_468
}

fn binary_op_to_c(op: BinaryOp) -> &'static str {
    match op {
        BinaryOp::Or => "|",
        BinaryOp::Xor => "^",
        BinaryOp::And => "&",
        BinaryOp::Equal => "==",
        BinaryOp::NotEqual => "!=",
        BinaryOp::Less => "<",
        BinaryOp::LessEqual => "<=",
        BinaryOp::Greater => ">",
        BinaryOp::GreaterEqual => ">=",
        BinaryOp::Add => "+",
        BinaryOp::Sub => "-",
        BinaryOp::Mul => "*",
        BinaryOp::Div => "/",
        BinaryOp::Mod => "%",
        BinaryOp::Power => "pow",
    }
}

fn binary_op_to_c_state(
    op: BinaryOp,
    left: &Expr,
    right: &Expr,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) -> &'static str {
    let bool_operands = expr_is_bool_for_c(left, var_types, project)
        && expr_is_bool_for_c(right, var_types, project);
    match op {
        BinaryOp::Or if bool_operands => "||",
        BinaryOp::Xor if bool_operands => "!=",
        BinaryOp::And if bool_operands => "&&",
        _ => binary_op_to_c(op),
    }
}

fn var_to_c(variable: &VariableRef) -> String {
    if let Some(direct) = &variable.direct {
        sanitize_c_ident(direct)
    } else {
        let mut text = variable
            .path
            .iter()
            .map(|part| sanitize_c_ident(&part.original))
            .collect::<Vec<_>>()
            .join("_");
        for indices in &variable.indices {
            for index in indices {
                write!(text, "[({}) - 1]", expr_to_c(index)).unwrap();
            }
        }
        text
    }
}

fn variable_spec(
    variable: &VariableRef,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) -> Option<DataTypeSpec> {
    if variable.direct.is_some() {
        return None;
    }
    let root = variable.root_name()?;
    let mut spec = var_types.get(&root.canonical)?.clone();
    spec = apply_indices_to_spec(
        spec,
        variable.indices.first().map(Vec::as_slice).unwrap_or(&[]),
        project,
    )?;

    for (segment_index, segment) in variable.path.iter().enumerate().skip(1) {
        let DataTypeSpec::Struct { fields } = resolve_named_spec(project, &spec) else {
            return None;
        };
        let field = fields
            .iter()
            .find(|field| field.name.canonical == segment.canonical)?;
        spec = apply_indices_to_spec(
            field.spec.clone(),
            variable
                .indices
                .get(segment_index)
                .map(Vec::as_slice)
                .unwrap_or(&[]),
            project,
        )?;
    }
    Some(spec)
}

fn expr_is_bool_for_c(
    expr: &Expr,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) -> bool {
    match expr {
        Expr::Literal(Literal::Bool(_)) => true,
        Expr::Variable(variable) => {
            variable_spec(variable, var_types, project).is_some_and(|spec| {
                matches!(
                    resolve_named_spec(project, &spec),
                    DataTypeSpec::Elementary(ElementaryType::Bool)
                )
            })
        }
        Expr::Unary {
            op: UnaryOp::Not,
            expr,
        } => expr_is_bool_for_c(expr, var_types, project),
        Expr::Binary { op, left, right } => match op {
            BinaryOp::Equal
            | BinaryOp::NotEqual
            | BinaryOp::Less
            | BinaryOp::LessEqual
            | BinaryOp::Greater
            | BinaryOp::GreaterEqual => true,
            BinaryOp::And | BinaryOp::Or | BinaryOp::Xor => {
                expr_is_bool_for_c(left, var_types, project)
                    && expr_is_bool_for_c(right, var_types, project)
            }
            _ => false,
        },
        Expr::Call { name, .. } => {
            matches!(
                name.canonical.as_str(),
                "GT" | "GE" | "EQ" | "NE" | "LE" | "LT" | "INT_TO_BOOL" | "STRING_TO_BOOL"
            ) || name.canonical.ends_with("_TO_BOOL")
        }
        _ => false,
    }
}

fn apply_indices_to_spec(
    spec: DataTypeSpec,
    indices: &[Expr],
    project: &Project,
) -> Option<DataTypeSpec> {
    if indices.is_empty() {
        return Some(spec);
    }
    let mut current = resolve_named_spec(project, &spec);
    let mut consumed = 0_usize;
    loop {
        let DataTypeSpec::Array {
            ranges,
            element_type,
        } = current
        else {
            return None;
        };
        consumed += ranges.len();
        current = *element_type;
        if consumed >= indices.len() {
            return Some(current);
        }
        current = resolve_named_spec(project, &current);
    }
}

