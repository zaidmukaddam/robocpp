use std::fmt::Write;

use iec_diagnostics::{Diagnostic, DiagnosticCode};
use iec_ir::*;

const RBCPP_DEFAULT_STRING_CAP: usize = 256;

#[derive(Debug, Clone)]
pub struct COutput {
    pub filename_hint: String,
    pub source: String,
}

pub fn generate_c(
    project: &Project,
    program_name: Option<&str>,
) -> Result<COutput, Vec<Diagnostic>> {
    let Some(program) = find_program(project, program_name) else {
        return Err(vec![Diagnostic::error(
            DiagnosticCode::Unsupported,
            "no PROGRAM POU found for C generation",
            None,
        )]);
    };

    let mut out = String::new();
    writeln!(out, "/* Generated by RoboC++ (rbcpp). */").unwrap();
    writeln!(out, "#include <math.h>").unwrap();
    writeln!(out, "#include <stdbool.h>").unwrap();
    writeln!(out, "#include <stdint.h>").unwrap();
    writeln!(out, "#include <stddef.h>").unwrap();
    writeln!(out, "#include <stdio.h>").unwrap();
    writeln!(out, "#include <stdlib.h>").unwrap();
    writeln!(out, "#include <string.h>").unwrap();
    writeln!(out, "#ifndef RBCPP_CYCLE_MS").unwrap();
    writeln!(out, "#define RBCPP_CYCLE_MS 1").unwrap();
    writeln!(out, "#endif").unwrap();
    writeln!(out, "#ifndef RBCPP_STRING_CAP").unwrap();
    writeln!(out, "#define RBCPP_STRING_CAP 256").unwrap();
    writeln!(out, "#endif").unwrap();
    writeln!(out, "#if defined(__GNUC__) || defined(__clang__)").unwrap();
    writeln!(out, "#define RBCPP_UNUSED __attribute__((unused))").unwrap();
    writeln!(out, "#else").unwrap();
    writeln!(out, "#define RBCPP_UNUSED").unwrap();
    writeln!(out, "#endif").unwrap();
    writeln!(out, "#define RBCPP_ABS(a) (((a) < 0) ? -(a) : (a))").unwrap();
    writeln!(out, "#define RBCPP_MIN(a, b) (((a) < (b)) ? (a) : (b))").unwrap();
    writeln!(out, "#define RBCPP_MAX(a, b) (((a) > (b)) ? (a) : (b))").unwrap();
    writeln!(
        out,
        "#define RBCPP_LIMIT(mn, in, mx) RBCPP_MIN(RBCPP_MAX((in), (mn)), (mx))"
    )
    .unwrap();
    writeln!(out, "#define RBCPP_SEL(g, a, b) ((g) ? (b) : (a))").unwrap();
    writeln!(
        out,
        "#define RBCPP_ROL64(v, n) ((int64_t)((((uint64_t)(v)) << ((n) & 63)) | (((uint64_t)(v)) >> ((64 - ((n) & 63)) & 63))))"
    )
    .unwrap();
    writeln!(
        out,
        "#define RBCPP_ROR64(v, n) ((int64_t)((((uint64_t)(v)) >> ((n) & 63)) | (((uint64_t)(v)) << ((64 - ((n) & 63)) & 63))))"
    )
    .unwrap();
    writeln!(out).unwrap();
    emit_bcd_helpers(&mut out);
    emit_string_helpers(&mut out);
    emit_data_type_declarations(&mut out, project);
    for function in project
        .pous()
        .filter(|pou| matches!(&pou.kind, PouKind::Function { .. }))
    {
        emit_function_pou(&mut out, function);
        writeln!(out).unwrap();
    }
    writeln!(out, "typedef struct {{").unwrap();
    for (var, _) in program_vars_with_retain(program) {
        if let Some(fields) = standard_fb_fields(&var.type_spec) {
            for field in fields {
                writeln!(
                    out,
                    "    {} {};",
                    field.c_type,
                    fb_field_ident(&var.name.original, field.name)
                )
                .unwrap();
            }
        } else if emit_aggregate_state_declaration(&mut out, var, project) {
        } else if let Some(function_block) = user_function_block(project, &var.type_spec) {
            for field in function_block.variable_declarations() {
                emit_c_declaration(
                    &mut out,
                    "    ",
                    &fb_field_ident(&var.name.original, &field.name.original),
                    &field.type_spec,
                    project,
                );
            }
        } else {
            writeln!(
                out,
                "    {} {};",
                c_type(&var.type_spec),
                sanitize_c_ident(&var.name.original)
            )
            .unwrap();
        }
    }
    if let Some(sfc) = &program.body.sfc {
        for step in &sfc.steps {
            writeln!(out, "    bool {};", sfc_step_field(&step.name)).unwrap();
        }
        for action in &sfc.actions {
            writeln!(out, "    bool {};", sfc_action_field(&action.name)).unwrap();
            writeln!(out, "    bool {};", sfc_action_previous_field(&action.name)).unwrap();
            writeln!(
                out,
                "    int64_t {};",
                sfc_action_elapsed_field(&action.name)
            )
            .unwrap();
        }
    }
    let state_type = format!("{}_state", sanitize_c_ident(&program.name.original));
    writeln!(out, "}} {state_type};").unwrap();
    emit_debug_metadata(&mut out, program, project);

    writeln!(out).unwrap();
    writeln!(
        out,
        "void {}_init({} *s) {{",
        sanitize_c_ident(&program.name.original),
        state_type
    )
    .unwrap();
    for (var, _) in program_vars_with_retain(program) {
        emit_state_initialization(&mut out, var, project);
    }
    if let Some(sfc) = &program.body.sfc {
        emit_sfc_initialization(&mut out, sfc);
    }
    writeln!(out, "}}").unwrap();

    writeln!(out).unwrap();
    writeln!(
        out,
        "void {}_warm_restart({} *s) {{",
        sanitize_c_ident(&program.name.original),
        state_type
    )
    .unwrap();
    for (var, retain) in program_vars_with_retain(program) {
        if retain != Some(RetainKind::Retain) {
            emit_state_initialization(&mut out, var, project);
        }
    }
    writeln!(out, "}}").unwrap();

    writeln!(out).unwrap();
    writeln!(
        out,
        "void {}_scan({} *s) {{",
        sanitize_c_ident(&program.name.original),
        state_type
    )
    .unwrap();
    let var_types = program
        .variable_declarations()
        .map(|var| (var.name.canonical.clone(), var.type_spec.clone()))
        .collect::<std::collections::BTreeMap<_, _>>();
    if statements_need_il_accumulator(&program.body.statements) {
        writeln!(out, "    int64_t rbcpp_acc = 0;").unwrap();
    }
    if let Some(sfc) = &program.body.sfc {
        emit_sfc_scan(&mut out, sfc, &var_types, project);
    } else {
        for statement in &program.body.statements {
            emit_statement(&mut out, statement, 1, &var_types, project);
        }
    }
    writeln!(out, "}}").unwrap();

    Ok(COutput {
        filename_hint: format!("{}.c", sanitize_c_ident(&program.name.original)),
        source: out,
    })
}

fn program_vars_with_retain(program: &Pou) -> impl Iterator<Item = (&VarDecl, Option<RetainKind>)> {
    program
        .var_blocks
        .iter()
        .flat_map(|block| block.vars.iter().map(move |var| (var, block.retain)))
}

fn user_function_block<'a>(project: &'a Project, spec: &DataTypeSpec) -> Option<&'a Pou> {
    let DataTypeSpec::Named(type_name) = spec else {
        return None;
    };
    project
        .find_pou(&type_name.original)
        .filter(|pou| matches!(&pou.kind, PouKind::FunctionBlock))
}

fn emit_bcd_helpers(out: &mut String) {
    writeln!(
        out,
        "static RBCPP_UNUSED int64_t rbcpp_bcd_to_int(int64_t input) {{"
    )
    .unwrap();
    writeln!(out, "    if (input < 0) {{ return 0; }}").unwrap();
    writeln!(out, "    uint64_t raw = (uint64_t)input;").unwrap();
    writeln!(out, "    int64_t result = 0;").unwrap();
    writeln!(out, "    int64_t place = 1;").unwrap();
    writeln!(out, "    while (raw != 0) {{").unwrap();
    writeln!(out, "        int64_t digit = (int64_t)(raw & 0x0fu);").unwrap();
    writeln!(out, "        if (digit > 9) {{ return 0; }}").unwrap();
    writeln!(out, "        result += digit * place;").unwrap();
    writeln!(out, "        place *= 10;").unwrap();
    writeln!(out, "        raw >>= 4;").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "    return result;").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED int64_t rbcpp_int_to_bcd(int64_t input) {{"
    )
    .unwrap();
    writeln!(out, "    if (input < 0) {{ return 0; }}").unwrap();
    writeln!(out, "    uint64_t raw = 0;").unwrap();
    writeln!(out, "    unsigned shift = 0;").unwrap();
    writeln!(out, "    while (input != 0 && shift < 64) {{").unwrap();
    writeln!(out, "        raw |= ((uint64_t)(input % 10)) << shift;").unwrap();
    writeln!(out, "        input /= 10;").unwrap();
    writeln!(out, "        shift += 4;").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "    return (int64_t)raw;").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
}

fn emit_string_helpers(out: &mut String) {
    writeln!(
        out,
        "static RBCPP_UNUSED void rbcpp_strassign(char *dest, size_t cap, const char *src) {{"
    )
    .unwrap();
    writeln!(out, "    if (cap == 0) {{ return; }}").unwrap();
    writeln!(out, "    snprintf(dest, cap, \"%s\", src ? src : \"\");").unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED char *rbcpp_strtmp(void) {{ static char buffers[8][RBCPP_STRING_CAP]; static unsigned index; index = (index + 1u) % 8u; buffers[index][0] = '\\0'; return buffers[index]; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_left(const char *s, int64_t len) {{ char *out = rbcpp_strtmp(); if (!s || len <= 0) {{ return out; }} snprintf(out, RBCPP_STRING_CAP, \"%.*s\", (int)len, s); return out; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_right(const char *s, int64_t len) {{ char *out = rbcpp_strtmp(); if (!s || len <= 0) {{ return out; }} size_t n = strlen(s); size_t take = (size_t)len < n ? (size_t)len : n; snprintf(out, RBCPP_STRING_CAP, \"%s\", s + (n - take)); return out; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_mid(const char *s, int64_t len, int64_t pos) {{ char *out = rbcpp_strtmp(); if (!s || len <= 0) {{ return out; }} size_t n = strlen(s); size_t start = pos <= 1 ? 0u : (size_t)(pos - 1); if (start > n) {{ start = n; }} snprintf(out, RBCPP_STRING_CAP, \"%.*s\", (int)len, s + start); return out; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_concat2(const char *a, const char *b) {{ char *out = rbcpp_strtmp(); snprintf(out, RBCPP_STRING_CAP, \"%s%s\", a ? a : \"\", b ? b : \"\"); return out; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_delete(const char *s, int64_t len, int64_t pos) {{ char *out = rbcpp_strtmp(); if (!s) {{ return out; }} size_t n = strlen(s); size_t start = pos <= 1 ? 0u : (size_t)(pos - 1); size_t drop = len <= 0 ? 0u : (size_t)len; if (start > n) {{ start = n; }} snprintf(out, RBCPP_STRING_CAP, \"%.*s%s\", (int)start, s, s + ((start + drop < n) ? start + drop : n)); return out; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_insert(const char *s, const char *ins, int64_t pos) {{ char *out = rbcpp_strtmp(); if (!s) {{ s = \"\"; }} if (!ins) {{ ins = \"\"; }} size_t n = strlen(s); size_t start = pos <= 1 ? 0u : (size_t)(pos - 1); if (start > n) {{ start = n; }} snprintf(out, RBCPP_STRING_CAP, \"%.*s%s%s\", (int)start, s, ins, s + start); return out; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_replace(const char *s, const char *rep, int64_t len, int64_t pos) {{ return rbcpp_insert(rbcpp_delete(s, len, pos), rep, pos); }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED int64_t rbcpp_find(const char *s, const char *needle) {{ if (!s || !needle) {{ return 0; }} const char *p = strstr(s, needle); return p ? (int64_t)(p - s + 1) : 0; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED bool rbcpp_string_to_bool(const char *s) {{ if (!s) {{ return false; }} return strcmp(s, \"TRUE\") == 0 || strcmp(s, \"true\") == 0 || strcmp(s, \"1\") == 0; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED int64_t rbcpp_string_to_int(const char *s) {{ return s ? (int64_t)strtoll(s, NULL, 10) : 0; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED double rbcpp_string_to_real(const char *s) {{ return s ? strtod(s, NULL) : 0.0; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED int64_t rbcpp_string_to_time(const char *s) {{ if (!s) {{ return 0; }} const char *p = s; if (strncmp(p, \"T#\", 2) == 0) {{ p += 2; }} else if (strncmp(p, \"TIME#\", 5) == 0) {{ p += 5; }} size_t n = strlen(p); if (n >= 2 && p[n - 2] == 'm' && p[n - 1] == 's') {{ return (int64_t)strtoll(p, NULL, 10); }} if (n >= 1 && p[n - 1] == 's') {{ return (int64_t)strtoll(p, NULL, 10) * 1000; }} return (int64_t)strtoll(p, NULL, 10); }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_bool_to_string(bool v) {{ return v ? \"TRUE\" : \"FALSE\"; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_int_to_string(int64_t v) {{ char *out = rbcpp_strtmp(); snprintf(out, RBCPP_STRING_CAP, \"%lld\", (long long)v); return out; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_real_to_string(double v) {{ char *out = rbcpp_strtmp(); snprintf(out, RBCPP_STRING_CAP, \"%.17g\", v); return out; }}"
    )
    .unwrap();
    writeln!(
        out,
        "static RBCPP_UNUSED const char *rbcpp_time_to_string(int64_t v) {{ char *out = rbcpp_strtmp(); snprintf(out, RBCPP_STRING_CAP, \"T#%lldms\", (long long)v); return out; }}"
    )
    .unwrap();
    writeln!(out).unwrap();
}

fn emit_data_type_declarations(out: &mut String, project: &Project) {
    for data_type in project.data_types() {
        let DataTypeSpec::Struct { fields } = &data_type.spec else {
            continue;
        };
        writeln!(out, "typedef struct {{").unwrap();
        for field in fields {
            emit_c_declaration(
                out,
                "    ",
                &sanitize_c_ident(&field.name.original),
                &field.spec,
                project,
            );
        }
        writeln!(out, "}} {};", type_c_ident(&data_type.name)).unwrap();
        writeln!(out).unwrap();
    }
}

fn emit_aggregate_state_declaration(out: &mut String, var: &VarDecl, project: &Project) -> bool {
    if !is_aggregate_spec(&var.type_spec, project) && !is_string_spec(project, &var.type_spec) {
        return false;
    }
    emit_c_declaration(
        out,
        "    ",
        &sanitize_c_ident(&var.name.original),
        &var.type_spec,
        project,
    );
    true
}

fn emit_c_declaration(
    out: &mut String,
    indent: &str,
    name: &str,
    spec: &DataTypeSpec,
    project: &Project,
) {
    let (base, dimensions) = peel_array_dimensions(project, spec);
    let dimensions = dimensions_to_c(&dimensions);

    if let Some(capacity) = c_string_capacity(project, &base) {
        writeln!(out, "{indent}char {name}{dimensions}[{capacity}];").unwrap();
        return;
    }

    if let Some(type_ident) = named_struct_type_ident(project, &base) {
        writeln!(out, "{indent}{type_ident} {name}{dimensions};").unwrap();
        return;
    }

    match resolve_named_spec(project, &base) {
        DataTypeSpec::Struct { fields } => {
            writeln!(out, "{indent}struct {{").unwrap();
            let nested_indent = format!("{indent}    ");
            for field in fields {
                emit_c_declaration(
                    out,
                    &nested_indent,
                    &sanitize_c_ident(&field.name.original),
                    &field.spec,
                    project,
                );
            }
            writeln!(out, "{indent}}} {name}{dimensions};").unwrap();
        }
        resolved => {
            writeln!(
                out,
                "{indent}{} {name}{dimensions};",
                c_storage_type(project, &resolved)
            )
            .unwrap();
        }
    }
}

fn emit_debug_metadata(out: &mut String, program: &Pou, project: &Project) {
    writeln!(out).unwrap();
    writeln!(
        out,
        "typedef struct {{ const char *name; const char *type; const char *storage; const char *c_type; const char *location; size_t elements; }} rbcpp_debug_symbol;"
    )
    .unwrap();
    writeln!(
        out,
        "static const rbcpp_debug_symbol rbcpp_debug_symbols[] RBCPP_UNUSED = {{"
    )
    .unwrap();
    for (var, retain) in program_vars_with_retain(program) {
        writeln!(
            out,
            "    {{\"{}\", \"{}\", \"{}\", \"{}\", \"{}\", {}}},",
            c_string_escape(&var.name.original),
            c_string_escape(&debug_type_name(project, &var.type_spec)),
            match retain {
                Some(RetainKind::Retain) => "retain",
                Some(RetainKind::NonRetain) => "non_retain",
                None => "plain",
            },
            c_string_escape(&debug_c_type_name(project, &var.type_spec)),
            c_string_escape(var.location.as_deref().unwrap_or("")),
            debug_element_count(project, &var.type_spec)
        )
        .unwrap();
    }
    if let Some(sfc) = &program.body.sfc {
        for step in &sfc.steps {
            writeln!(
                out,
                "    {{\"{}\", \"SFC_STEP\", \"sfc\", \"bool\", \"\", 1}},",
                c_string_escape(&sfc_step_field(&step.name))
            )
            .unwrap();
        }
        for action in &sfc.actions {
            writeln!(
                out,
                "    {{\"{}\", \"SFC_ACTION_{}\", \"sfc\", \"bool\", \"\", 1}},",
                c_string_escape(&sfc_action_field(&action.name)),
                action.qualifier.as_iec()
            )
            .unwrap();
        }
    }
    writeln!(out, "}};").unwrap();
    writeln!(
        out,
        "static const size_t rbcpp_debug_symbol_count RBCPP_UNUSED = sizeof(rbcpp_debug_symbols) / sizeof(rbcpp_debug_symbols[0]);"
    )
    .unwrap();
}

fn debug_type_name(project: &Project, spec: &DataTypeSpec) -> String {
    match resolve_named_spec(project, spec) {
        DataTypeSpec::Elementary(elementary) => elementary.as_iec().to_string(),
        DataTypeSpec::Named(name) => name.original,
        DataTypeSpec::Array {
            ranges,
            element_type,
        } => {
            let ranges = ranges
                .iter()
                .map(|range| format!("{}..{}", range.low, range.high))
                .collect::<Vec<_>>()
                .join(",");
            format!(
                "ARRAY[{ranges}] OF {}",
                debug_type_name(project, &element_type)
            )
        }
        DataTypeSpec::Struct { fields } => {
            let fields = fields
                .iter()
                .map(|field| {
                    format!(
                        "{}:{}",
                        field.name.original,
                        debug_type_name(project, &field.spec)
                    )
                })
                .collect::<Vec<_>>()
                .join(";");
            format!("STRUCT{{{fields}}}")
        }
        DataTypeSpec::Enum { .. } => "ENUM".to_string(),
        DataTypeSpec::Subrange { base, range } => {
            format!("{}({}..{})", base.as_iec(), range.low, range.high)
        }
        DataTypeSpec::String { wide, length } => {
            let prefix = if wide { "WSTRING" } else { "STRING" };
            length
                .map(|length| format!("{prefix}[{length}]"))
                .unwrap_or_else(|| prefix.to_string())
        }
    }
}

fn debug_c_type_name(project: &Project, spec: &DataTypeSpec) -> String {
    if let Some(type_ident) = named_struct_type_ident(project, spec) {
        return type_ident;
    }
    let resolved = resolve_named_spec(project, spec);
    if matches!(resolved, DataTypeSpec::Array { .. }) {
        let (element, dimensions) = peel_array_dimensions(project, &resolved);
        return format!(
            "{}{}",
            c_storage_type(project, &element),
            dimensions_to_c(&dimensions)
        );
    }
    c_storage_type(project, &resolved).to_string()
}

fn debug_element_count(project: &Project, spec: &DataTypeSpec) -> usize {
    match resolve_named_spec(project, spec) {
        DataTypeSpec::Array { ranges, .. } => array_element_count(&ranges),
        DataTypeSpec::String { length, .. } => length.unwrap_or(RBCPP_DEFAULT_STRING_CAP),
        DataTypeSpec::Elementary(ElementaryType::String | ElementaryType::WString) => {
            RBCPP_DEFAULT_STRING_CAP
        }
        _ => 1,
    }
}

fn c_string_escape(input: &str) -> String {
    input.replace('\\', "\\\\").replace('"', "\\\"")
}

fn emit_aggregate_state_initialization(
    out: &mut String,
    var_name: &str,
    spec: &DataTypeSpec,
    initial: Option<&Expr>,
    project: &Project,
) -> bool {
    if !is_aggregate_spec(spec, project) && !is_string_spec(project, spec) {
        return false;
    }
    let target = format!("s->{}", sanitize_c_ident(var_name));
    emit_initializer(out, &target, spec, initial, project);
    true
}

fn emit_initializer(
    out: &mut String,
    target: &str,
    spec: &DataTypeSpec,
    initial: Option<&Expr>,
    project: &Project,
) {
    match resolve_named_spec(project, spec) {
        DataTypeSpec::Elementary(ElementaryType::String | ElementaryType::WString)
        | DataTypeSpec::String { .. } => {
            let value = initial
                .map(|expr| initializer_expr_to_c(expr, spec, project))
                .unwrap_or_else(|| "\"\"".to_string());
            let capacity = c_string_capacity(project, spec).unwrap_or(RBCPP_DEFAULT_STRING_CAP);
            writeln!(out, "    rbcpp_strassign({target}, {capacity}, {value});").unwrap();
        }
        DataTypeSpec::Array {
            ranges,
            element_type,
        } => {
            let elements = match initial {
                Some(Expr::ArrayLiteral(elements)) => elements.as_slice(),
                _ => &[],
            };
            let total = array_element_count(&ranges);
            for offset in 0..total {
                let indexed_target = format!("{target}{}", c_zero_based_indices(offset, &ranges));
                emit_initializer(
                    out,
                    &indexed_target,
                    &element_type,
                    elements.get(offset),
                    project,
                );
            }
        }
        DataTypeSpec::Struct { fields } => {
            let initializers = match initial {
                Some(Expr::StructLiteral(initializers)) => initializers.as_slice(),
                _ => &[],
            };
            for field in fields {
                let initializer = initializers
                    .iter()
                    .find(|initializer| {
                        initializer
                            .name
                            .as_ref()
                            .is_some_and(|name| name.canonical == field.name.canonical)
                    })
                    .and_then(|initializer| initializer.expr.as_ref())
                    .or(field.initial_value.as_ref());
                let field_target = format!("{target}.{}", sanitize_c_ident(&field.name.original));
                emit_initializer(out, &field_target, &field.spec, initializer, project);
            }
        }
        DataTypeSpec::Enum { .. } => {
            let value = initial
                .map(|expr| initializer_expr_to_c(expr, spec, project))
                .unwrap_or_else(|| "0".to_string());
            writeln!(out, "    {target} = {value};").unwrap();
        }
        DataTypeSpec::Subrange { range, .. } => {
            let value = initial
                .map(|expr| initializer_expr_to_c(expr, spec, project))
                .unwrap_or_else(|| {
                    if range.low <= 0 && range.high >= 0 {
                        "0".to_string()
                    } else {
                        range.low.to_string()
                    }
                });
            writeln!(out, "    {target} = {value};").unwrap();
        }
        resolved => {
            let value = initial
                .map(|expr| initializer_expr_to_c(expr, &resolved, project))
                .unwrap_or_else(|| c_default(&resolved).to_string());
            writeln!(out, "    {target} = {value};").unwrap();
        }
    }
}

fn emit_state_initialization(out: &mut String, var: &VarDecl, project: &Project) {
    if let Some(fields) = standard_fb_fields(&var.type_spec) {
        for field in fields {
            writeln!(
                out,
                "    s->{} = {};",
                fb_field_ident(&var.name.original, field.name),
                field.default
            )
            .unwrap();
        }
    } else if emit_aggregate_state_initialization(
        out,
        &var.name.original,
        &var.type_spec,
        var.initial_value.as_ref(),
        project,
    ) {
    } else if let Some(function_block) = user_function_block(project, &var.type_spec) {
        for field in function_block.variable_declarations() {
            if is_aggregate_spec(&field.type_spec, project)
                || is_string_spec(project, &field.type_spec)
            {
                let target = format!(
                    "s->{}",
                    fb_field_ident(&var.name.original, &field.name.original)
                );
                emit_initializer(
                    out,
                    &target,
                    &field.type_spec,
                    field.initial_value.as_ref(),
                    project,
                );
                continue;
            }
            let initial = field
                .initial_value
                .as_ref()
                .map(|expr| initializer_expr_to_c(expr, &field.type_spec, project))
                .unwrap_or_else(|| c_default(&field.type_spec).to_string());
            writeln!(
                out,
                "    s->{} = {};",
                fb_field_ident(&var.name.original, &field.name.original),
                initial
            )
            .unwrap();
        }
    } else if let Some(initial) = &var.initial_value {
        writeln!(
            out,
            "    s->{} = {};",
            sanitize_c_ident(&var.name.original),
            initializer_expr_to_c(initial, &var.type_spec, project)
        )
        .unwrap();
    } else {
        writeln!(
            out,
            "    s->{} = {};",
            sanitize_c_ident(&var.name.original),
            c_default(&var.type_spec)
        )
        .unwrap();
    }
}

fn emit_function_pou(out: &mut String, function: &Pou) {
    let PouKind::Function { return_type } = &function.kind else {
        return;
    };
    let parameters = function
        .var_blocks
        .iter()
        .filter(|block| block.kind == VarBlockKind::Input)
        .flat_map(|block| block.vars.iter())
        .map(|var| {
            format!(
                "{} {}",
                c_type(&var.type_spec),
                sanitize_c_ident(&var.name.original)
            )
        })
        .collect::<Vec<_>>();
    let parameter_list = if parameters.is_empty() {
        "void".to_string()
    } else {
        parameters.join(", ")
    };
    let return_name = sanitize_c_ident(&function.name.original);
    writeln!(
        out,
        "static {} {}({}) {{",
        c_type(return_type),
        return_name,
        parameter_list
    )
    .unwrap();
    writeln!(
        out,
        "    {} {} = {};",
        c_type(return_type),
        return_name,
        c_default(return_type)
    )
    .unwrap();

    let input_names = function
        .var_blocks
        .iter()
        .filter(|block| block.kind == VarBlockKind::Input)
        .flat_map(|block| block.vars.iter())
        .map(|var| var.name.canonical.clone())
        .collect::<std::collections::BTreeSet<_>>();
    for var in function.variable_declarations() {
        if input_names.contains(&var.name.canonical) {
            continue;
        }
        let initial = var
            .initial_value
            .as_ref()
            .map(expr_to_c_local)
            .unwrap_or_else(|| c_default(&var.type_spec).to_string());
        writeln!(
            out,
            "    {} {} = {};",
            c_type(&var.type_spec),
            sanitize_c_ident(&var.name.original),
            initial
        )
        .unwrap();
    }

    if statements_need_il_accumulator(&function.body.statements) {
        writeln!(out, "    int64_t rbcpp_acc = 0;").unwrap();
    }
    for statement in &function.body.statements {
        emit_function_statement(out, statement, 1, &return_name);
    }
    writeln!(out, "    return {return_name};").unwrap();
    writeln!(out, "}}").unwrap();
}

fn find_program<'a>(project: &'a Project, program_name: Option<&str>) -> Option<&'a Pou> {
    if let Some(name) = program_name {
        project
            .find_pou(name)
            .filter(|pou| matches!(&pou.kind, PouKind::Program))
    } else {
        project.first_program()
    }
}

fn emit_statement(
    out: &mut String,
    statement: &Statement,
    indent: usize,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) {
    let pad = "    ".repeat(indent);
    match statement {
        Statement::Assignment { target, value } => {
            let target_c = var_to_c_state(target, var_types, project);
            if let Some(target_spec) = variable_spec(target, var_types, project) {
                if let Some(capacity) = c_string_capacity(project, &target_spec) {
                    writeln!(
                        out,
                        "{pad}rbcpp_strassign(s->{target_c}, {capacity}, {});",
                        expr_to_c_state(value, var_types, project)
                    )
                    .unwrap();
                    return;
                }
            }
            writeln!(
                out,
                "{pad}s->{} = {};",
                target_c,
                expr_to_c_state(value, var_types, project)
            )
            .unwrap();
        }
        Statement::If {
            branches,
            else_branch,
        } => {
            for (index, (condition, body)) in branches.iter().enumerate() {
                if index == 0 {
                    writeln!(
                        out,
                        "{pad}if ({}) {{",
                        expr_to_c_state(condition, var_types, project)
                    )
                    .unwrap();
                } else {
                    writeln!(
                        out,
                        "{pad}else if ({}) {{",
                        expr_to_c_state(condition, var_types, project)
                    )
                    .unwrap();
                }
                for statement in body {
                    emit_statement(out, statement, indent + 1, var_types, project);
                }
                writeln!(out, "{pad}}}").unwrap();
            }
            if !else_branch.is_empty() {
                writeln!(out, "{pad}else {{").unwrap();
                for statement in else_branch {
                    emit_statement(out, statement, indent + 1, var_types, project);
                }
                writeln!(out, "{pad}}}").unwrap();
            }
        }
        Statement::For {
            control,
            from,
            to,
            by,
            body,
        } => {
            let step = by
                .as_ref()
                .map(|expr| expr_to_c_state(expr, var_types, project))
                .unwrap_or_else(|| "1".to_string());
            let control_name = sanitize_c_ident(&control.original);
            writeln!(out, "{pad}{{").unwrap();
            writeln!(out, "{pad}    int64_t rbcpp_step = {step};").unwrap();
            writeln!(
                out,
                "{pad}    for (s->{control_name} = {}; (rbcpp_step >= 0) ? (s->{control_name} <= {}) : (s->{control_name} >= {}); s->{control_name} += rbcpp_step) {{",
                expr_to_c_state(from, var_types, project),
                expr_to_c_state(to, var_types, project),
                expr_to_c_state(to, var_types, project)
            )
            .unwrap();
            for statement in body {
                emit_statement(out, statement, indent + 2, var_types, project);
            }
            writeln!(out, "{pad}    }}").unwrap();
            writeln!(out, "{pad}}}").unwrap();
        }
        Statement::While { condition, body } => {
            writeln!(
                out,
                "{pad}while ({}) {{",
                expr_to_c_state(condition, var_types, project)
            )
            .unwrap();
            for statement in body {
                emit_statement(out, statement, indent + 1, var_types, project);
            }
            writeln!(out, "{pad}}}").unwrap();
        }
        Statement::Repeat { body, until } => {
            writeln!(out, "{pad}do {{").unwrap();
            for statement in body {
                emit_statement(out, statement, indent + 1, var_types, project);
            }
            writeln!(
                out,
                "{pad}}} while (!({}));",
                expr_to_c_state(until, var_types, project)
            )
            .unwrap();
        }
        Statement::Il { op, operand } => {
            emit_il_instruction(out, &pad, *op, operand.as_ref(), var_types, project)
        }
        Statement::IlLabel(label) => {
            writeln!(out, "{}:;", il_label_to_c(label)).unwrap();
        }
        Statement::Exit => {
            writeln!(out, "{pad}break;").unwrap();
        }
        Statement::Return => {
            writeln!(out, "{pad}return;").unwrap();
        }
        Statement::FbCall { name, args } => {
            emit_fb_call(out, &pad, name, args, var_types, project);
        }
        Statement::Case {
            selector,
            cases,
            else_branch,
        } => emit_case_statement(
            out,
            &pad,
            selector,
            cases,
            else_branch,
            indent,
            var_types,
            project,
        ),
        Statement::Unsupported(_) | Statement::Empty => {
            writeln!(out, "{pad}/* statement not emitted yet */").unwrap();
        }
    }
}

fn emit_case_statement(
    out: &mut String,
    pad: &str,
    selector: &Expr,
    cases: &[(Vec<CaseLabel>, Vec<Statement>)],
    else_branch: &[Statement],
    indent: usize,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) {
    let selector_c = expr_to_c_state(selector, var_types, project);
    for (index, (labels, body)) in cases.iter().enumerate() {
        let condition = labels
            .iter()
            .map(|label| case_label_to_c(&selector_c, label, var_types, project))
            .collect::<Vec<_>>()
            .join(" || ");
        if index == 0 {
            writeln!(out, "{pad}if ({condition}) {{").unwrap();
        } else {
            writeln!(out, "{pad}else if ({condition}) {{").unwrap();
        }
        for statement in body {
            emit_statement(out, statement, indent + 1, var_types, project);
        }
        writeln!(out, "{pad}}}").unwrap();
    }

    if !else_branch.is_empty() {
        writeln!(out, "{pad}else {{").unwrap();
        for statement in else_branch {
            emit_statement(out, statement, indent + 1, var_types, project);
        }
        writeln!(out, "{pad}}}").unwrap();
    }
}

fn case_label_to_c(
    selector_c: &str,
    label: &CaseLabel,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) -> String {
    match label {
        CaseLabel::Single(expr) => {
            format!(
                "{selector_c} == {}",
                expr_to_c_state(expr, var_types, project)
            )
        }
        CaseLabel::Range(low, high) => format!(
            "({selector_c} >= {}) && ({selector_c} <= {})",
            expr_to_c_state(low, var_types, project),
            expr_to_c_state(high, var_types, project)
        ),
    }
}

fn emit_sfc_initialization(out: &mut String, sfc: &Sfc) {
    for step in &sfc.steps {
        writeln!(
            out,
            "    s->{} = {};",
            sfc_step_field(&step.name),
            if step.initial { "true" } else { "false" }
        )
        .unwrap();
    }
    for action in &sfc.actions {
        writeln!(out, "    s->{} = false;", sfc_action_field(&action.name)).unwrap();
        writeln!(
            out,
            "    s->{} = false;",
            sfc_action_previous_field(&action.name)
        )
        .unwrap();
        writeln!(
            out,
            "    s->{} = 0;",
            sfc_action_elapsed_field(&action.name)
        )
        .unwrap();
    }
}

fn emit_sfc_scan(
    out: &mut String,
    sfc: &Sfc,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) {
    for action in &sfc.actions {
        emit_sfc_action_execution(out, sfc, action, var_types, project);
    }

    for (index, transition) in sfc.transitions.iter().enumerate() {
        let Some(from_step) = sfc.steps.get(index) else {
            continue;
        };
        let Some(to_step) = sfc.steps.get(index + 1) else {
            continue;
        };
        let condition = transition
            .condition
            .as_ref()
            .map(|expr| expr_to_c_state(expr, var_types, project))
            .unwrap_or_else(|| "false".to_string());
        writeln!(
            out,
            "    if (s->{} && ({})) {{",
            sfc_step_field(&from_step.name),
            condition
        )
        .unwrap();
        writeln!(
            out,
            "        s->{} = false;",
            sfc_step_field(&from_step.name)
        )
        .unwrap();
        writeln!(out, "        s->{} = true;", sfc_step_field(&to_step.name)).unwrap();
        writeln!(out, "    }}").unwrap();
    }
}

fn emit_sfc_action_execution(
    out: &mut String,
    sfc: &Sfc,
    action: &SfcAction,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) {
    let active = sfc
        .steps
        .iter()
        .find(|step| step.name.canonical == action.name.canonical)
        .map(|step| format!("s->{}", sfc_step_field(&step.name)))
        .unwrap_or_else(|| "false".to_string());
    let stored = sfc_action_field(&action.name);
    let previous = sfc_action_previous_field(&action.name);
    let elapsed = sfc_action_elapsed_field(&action.name);
    let duration = sfc_action_duration_ms(action);

    match action.qualifier {
        SfcActionQualifier::NonStored => {
            writeln!(out, "    if ({active}) {{").unwrap();
            emit_sfc_action_body(out, action, 2, var_types, project);
            writeln!(out, "    }}").unwrap();
        }
        SfcActionQualifier::Pulse => {
            writeln!(out, "    if ({active} && !s->{previous}) {{").unwrap();
            emit_sfc_action_body(out, action, 2, var_types, project);
            writeln!(out, "    }}").unwrap();
            writeln!(out, "    s->{previous} = {active};").unwrap();
        }
        SfcActionQualifier::SetStored => {
            writeln!(out, "    if ({active}) {{ s->{stored} = true; }}").unwrap();
            writeln!(out, "    if (s->{stored}) {{").unwrap();
            emit_sfc_action_body(out, action, 2, var_types, project);
            writeln!(out, "    }}").unwrap();
        }
        SfcActionQualifier::ResetStored => {
            writeln!(out, "    if ({active}) {{").unwrap();
            writeln!(out, "        s->{stored} = false;").unwrap();
            writeln!(out, "        s->{elapsed} = 0;").unwrap();
            writeln!(out, "    }}").unwrap();
        }
        SfcActionQualifier::TimeLimited => {
            writeln!(out, "    if ({active}) {{").unwrap();
            writeln!(out, "        s->{elapsed} += RBCPP_CYCLE_MS;").unwrap();
            writeln!(out, "        if (s->{elapsed} <= {duration}) {{").unwrap();
            emit_sfc_action_body(out, action, 3, var_types, project);
            writeln!(out, "        }}").unwrap();
            writeln!(out, "    }} else {{").unwrap();
            writeln!(out, "        s->{elapsed} = 0;").unwrap();
            writeln!(out, "    }}").unwrap();
        }
        SfcActionQualifier::TimeDelayed => {
            writeln!(out, "    if ({active}) {{").unwrap();
            writeln!(out, "        s->{elapsed} += RBCPP_CYCLE_MS;").unwrap();
            writeln!(out, "        if (s->{elapsed} >= {duration}) {{").unwrap();
            emit_sfc_action_body(out, action, 3, var_types, project);
            writeln!(out, "        }}").unwrap();
            writeln!(out, "    }} else {{").unwrap();
            writeln!(out, "        s->{elapsed} = 0;").unwrap();
            writeln!(out, "    }}").unwrap();
        }
        SfcActionQualifier::StoredDelayed | SfcActionQualifier::DelayedStored => {
            writeln!(out, "    if ({active}) {{").unwrap();
            writeln!(out, "        s->{elapsed} += RBCPP_CYCLE_MS;").unwrap();
            writeln!(
                out,
                "        if (s->{elapsed} >= {duration}) {{ s->{stored} = true; }}"
            )
            .unwrap();
            writeln!(out, "    }} else {{").unwrap();
            writeln!(out, "        s->{elapsed} = 0;").unwrap();
            writeln!(out, "    }}").unwrap();
            writeln!(out, "    if (s->{stored}) {{").unwrap();
            emit_sfc_action_body(out, action, 2, var_types, project);
            writeln!(out, "    }}").unwrap();
        }
        SfcActionQualifier::StoredLimited => {
            writeln!(out, "    if ({active} && !s->{stored}) {{").unwrap();
            writeln!(out, "        s->{stored} = true;").unwrap();
            writeln!(out, "        s->{elapsed} = 0;").unwrap();
            writeln!(out, "    }}").unwrap();
            writeln!(out, "    if (s->{stored}) {{").unwrap();
            writeln!(out, "        s->{elapsed} += RBCPP_CYCLE_MS;").unwrap();
            writeln!(out, "        if (s->{elapsed} <= {duration}) {{").unwrap();
            emit_sfc_action_body(out, action, 3, var_types, project);
            writeln!(out, "        }} else {{").unwrap();
            writeln!(out, "            s->{stored} = false;").unwrap();
            writeln!(out, "        }}").unwrap();
            writeln!(out, "    }}").unwrap();
        }
    }
}

fn emit_sfc_action_body(
    out: &mut String,
    action: &SfcAction,
    indent: usize,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) {
    for statement in &action.body {
        emit_statement(out, statement, indent, var_types, project);
    }
}

fn emit_function_statement(
    out: &mut String,
    statement: &Statement,
    indent: usize,
    return_name: &str,
) {
    let pad = "    ".repeat(indent);
    match statement {
        Statement::Assignment { target, value } => {
            writeln!(
                out,
                "{pad}{} = {};",
                local_var_to_c(target),
                expr_to_c_local(value)
            )
            .unwrap();
        }
        Statement::If {
            branches,
            else_branch,
        } => {
            for (index, (condition, body)) in branches.iter().enumerate() {
                if index == 0 {
                    writeln!(out, "{pad}if ({}) {{", expr_to_c_local(condition)).unwrap();
                } else {
                    writeln!(out, "{pad}else if ({}) {{", expr_to_c_local(condition)).unwrap();
                }
                for statement in body {
                    emit_function_statement(out, statement, indent + 1, return_name);
                }
                writeln!(out, "{pad}}}").unwrap();
            }
            if !else_branch.is_empty() {
                writeln!(out, "{pad}else {{").unwrap();
                for statement in else_branch {
                    emit_function_statement(out, statement, indent + 1, return_name);
                }
                writeln!(out, "{pad}}}").unwrap();
            }
        }
        Statement::Case {
            selector,
            cases,
            else_branch,
        } => emit_function_case_statement(
            out,
            &pad,
            selector,
            cases,
            else_branch,
            indent,
            return_name,
        ),
        Statement::For {
            control,
            from,
            to,
            by,
            body,
        } => {
            let step = by
                .as_ref()
                .map(expr_to_c_local)
                .unwrap_or_else(|| "1".to_string());
            let control_name = sanitize_c_ident(&control.original);
            writeln!(out, "{pad}{{").unwrap();
            writeln!(out, "{pad}    int64_t rbcpp_step = {step};").unwrap();
            writeln!(
                out,
                "{pad}    for ({control_name} = {}; (rbcpp_step >= 0) ? ({control_name} <= {}) : ({control_name} >= {}); {control_name} += rbcpp_step) {{",
                expr_to_c_local(from),
                expr_to_c_local(to),
                expr_to_c_local(to)
            )
            .unwrap();
            for statement in body {
                emit_function_statement(out, statement, indent + 2, return_name);
            }
            writeln!(out, "{pad}    }}").unwrap();
            writeln!(out, "{pad}}}").unwrap();
        }
        Statement::While { condition, body } => {
            writeln!(out, "{pad}while ({}) {{", expr_to_c_local(condition)).unwrap();
            for statement in body {
                emit_function_statement(out, statement, indent + 1, return_name);
            }
            writeln!(out, "{pad}}}").unwrap();
        }
        Statement::Repeat { body, until } => {
            writeln!(out, "{pad}do {{").unwrap();
            for statement in body {
                emit_function_statement(out, statement, indent + 1, return_name);
            }
            writeln!(out, "{pad}}} while (!({}));", expr_to_c_local(until)).unwrap();
        }
        Statement::Il { op, operand } => {
            emit_local_il_instruction(out, &pad, *op, operand.as_ref())
        }
        Statement::IlLabel(label) => {
            writeln!(out, "{}:;", il_label_to_c(label)).unwrap();
        }
        Statement::Exit => {
            writeln!(out, "{pad}break;").unwrap();
        }
        Statement::Return => {
            writeln!(out, "{pad}return {return_name};").unwrap();
        }
        Statement::FbCall { .. } | Statement::Unsupported(_) | Statement::Empty => {
            writeln!(out, "{pad}/* statement not emitted yet */").unwrap();
        }
    }
}

fn emit_function_case_statement(
    out: &mut String,
    pad: &str,
    selector: &Expr,
    cases: &[(Vec<CaseLabel>, Vec<Statement>)],
    else_branch: &[Statement],
    indent: usize,
    return_name: &str,
) {
    let selector_c = expr_to_c_local(selector);
    for (index, (labels, body)) in cases.iter().enumerate() {
        let condition = labels
            .iter()
            .map(|label| case_label_to_c_local(&selector_c, label))
            .collect::<Vec<_>>()
            .join(" || ");
        if index == 0 {
            writeln!(out, "{pad}if ({condition}) {{").unwrap();
        } else {
            writeln!(out, "{pad}else if ({condition}) {{").unwrap();
        }
        for statement in body {
            emit_function_statement(out, statement, indent + 1, return_name);
        }
        writeln!(out, "{pad}}}").unwrap();
    }

    if !else_branch.is_empty() {
        writeln!(out, "{pad}else {{").unwrap();
        for statement in else_branch {
            emit_function_statement(out, statement, indent + 1, return_name);
        }
        writeln!(out, "{pad}}}").unwrap();
    }
}

fn case_label_to_c_local(selector_c: &str, label: &CaseLabel) -> String {
    match label {
        CaseLabel::Single(expr) => format!("{selector_c} == {}", expr_to_c_local(expr)),
        CaseLabel::Range(low, high) => format!(
            "({selector_c} >= {}) && ({selector_c} <= {})",
            expr_to_c_local(low),
            expr_to_c_local(high)
        ),
    }
}

fn statements_need_il_accumulator(statements: &[Statement]) -> bool {
    statements.iter().any(statement_needs_il_accumulator)
}

fn statement_needs_il_accumulator(statement: &Statement) -> bool {
    match statement {
        Statement::Il { .. } => true,
        Statement::If {
            branches,
            else_branch,
        } => {
            branches
                .iter()
                .any(|(_, body)| statements_need_il_accumulator(body))
                || statements_need_il_accumulator(else_branch)
        }
        Statement::Case {
            cases, else_branch, ..
        } => {
            cases
                .iter()
                .any(|(_, body)| statements_need_il_accumulator(body))
                || statements_need_il_accumulator(else_branch)
        }
        Statement::For { body, .. }
        | Statement::While { body, .. }
        | Statement::Repeat { body, .. } => statements_need_il_accumulator(body),
        _ => false,
    }
}

fn emit_il_instruction(
    out: &mut String,
    pad: &str,
    op: IlOp,
    operand: Option<&Expr>,
    var_types: &std::collections::BTreeMap<String, DataTypeSpec>,
    project: &Project,
) {
    let operand_c = operand
        .map(|expr| expr_to_c_state(expr, var_types, project))
        .unwrap_or_else(|| "0".to_string());
    match op {
        IlOp::Ld => writeln!(out, "{pad}rbcpp_acc = {operand_c};").unwrap(),
        IlOp::Ldn => writeln!(out, "{pad}rbcpp_acc = !({operand_c});").unwrap(),
        IlOp::St => {
            if let Some(Expr::Variable(target)) = operand {
                writeln!(
                    out,
                    "{pad}s->{} = rbcpp_acc;",
                    var_to_c_state(target, var_types, project)
                )
                .unwrap();
            }
        }
        IlOp::Stn => {
            if let Some(Expr::Variable(target)) = operand {
                writeln!(
                    out,
                    "{pad}s->{} = !rbcpp_acc;",
                    var_to_c_state(target, var_types, project)
                )
                .unwrap();
            }
        }
        IlOp::S | IlOp::R => {
            if let Some(Expr::Variable(target)) = operand {
                let value = if matches!(op, IlOp::S) {
                    "true"
                } else {
                    "false"
                };
                writeln!(
                    out,
                    "{pad}if (rbcpp_acc) {{ s->{} = {value}; }}",
                    var_to_c_state(target, var_types, project)
                )
                .unwrap();
            }
        }
        IlOp::Not => writeln!(out, "{pad}rbcpp_acc = !rbcpp_acc;").unwrap(),
        IlOp::And => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc && ({operand_c});").unwrap(),
        IlOp::Andn => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc && !({operand_c});").unwrap(),
        IlOp::Or => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc || ({operand_c});").unwrap(),
        IlOp::Orn => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc || !({operand_c});").unwrap(),
        IlOp::Xor => writeln!(out, "{pad}rbcpp_acc = !!rbcpp_acc != !!({operand_c});").unwrap(),
        IlOp::Xorn => writeln!(out, "{pad}rbcpp_acc = !!rbcpp_acc != !({operand_c});").unwrap(),
        IlOp::Add => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc + ({operand_c});").unwrap(),
        IlOp::Sub => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc - ({operand_c});").unwrap(),
        IlOp::Mul => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc * ({operand_c});").unwrap(),
        IlOp::Div => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc / ({operand_c});").unwrap(),
        IlOp::Mod => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc % ({operand_c});").unwrap(),
        IlOp::Gt => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc > ({operand_c});").unwrap(),
        IlOp::Ge => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc >= ({operand_c});").unwrap(),
        IlOp::Eq => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc == ({operand_c});").unwrap(),
        IlOp::Ne => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc != ({operand_c});").unwrap(),
        IlOp::Le => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc <= ({operand_c});").unwrap(),
        IlOp::Lt => writeln!(out, "{pad}rbcpp_acc = rbcpp_acc < ({operand_c});").unwrap(),
        IlOp::Jmp | IlOp::Jmpc | IlOp::Jmpcn => {
            if let Some(label) = operand.and_then(il_label_operand) {
                let label = il_label_to_c(label);
                match op {
                    IlOp::Jmp => writeln!(out, "{pad}goto {label};").unwrap(),
                    IlOp::Jmpc => writeln!(out, "{pad}if (rbcpp_acc) {{ goto {label}; }}").unwrap(),
                    IlOp::Jmpcn => {
                        writeln!(out, "{pad}if (!rbcpp_acc) {{ goto {label}; }}").unwrap();
                    }
                    _ => {}
                }
            }
        }
        IlOp::Cal | IlOp::Calc | IlOp::Calcn => {
            if let Some((name, args)) = operand.and_then(il_call_operand) {
                match op {
                    IlOp::Cal => emit_fb_call(out, pad, &name, &args, var_types, project),
                    IlOp::Calc => {
                        writeln!(out, "{pad}if (rbcpp_acc) {{").unwrap();
                        emit_fb_call(out, &format!("{pad}    "), &name, &args, var_types, project);
                        writeln!(out, "{pad}}}").unwrap();
                    }
                    IlOp::Calcn => {
                        writeln!(out, "{pad}if (!rbcpp_acc) {{").unwrap();
                        emit_fb_call(out, &format!("{pad}    "), &name, &args, var_types, project);
                        writeln!(out, "{pad}}}").unwrap();
                    }
                    _ => {}
                }
            }
        }
        IlOp::Ret => writeln!(out, "{pad}return;").unwrap(),
        IlOp::Retc => writeln!(out, "{pad}if (rbcpp_acc) {{ return; }}").unwrap(),
        IlOp::Retcn => writeln!(out, "{pad}if (!rbcpp_acc) {{ return; }}").unwrap(),
    }
}

